#For e While
# Loop for
for i in range(10):
    print(i)



# Loop while
contador = 0
while contador < 5:
    print(contador)
    contador += 1


#Analisar todos os funcionários
# Iterando sobre uma lista de funcionários
funcionarios = ["Alice", "Bob", "Carol"]
for funcionario in funcionarios:
    print(funcionario)


# Analisar todos os meses de vendas
# Iterando sobre uma lista de meses
meses = ["Janeiro", "Fevereiro", "Março"]
for mes in meses:
    print(mes)


#Analisar todos os preços dos produtos
# Iterando sobre uma lista de preços
precos = [10.99, 20.99, 30.99]
for preco in precos:
    print(preco)

